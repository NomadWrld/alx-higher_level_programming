#!/usr/bin/python3

def add(a, b):
    """
    Adds two integers and returns the result.

    Args:
        a: An integer.
        b: An integer.

    Returns:
        The sum of a and b.
    """
    return a + b

# Test the function
if __name__ == "__main__":
    print(add(1, 2))
    print(add(98, 0))
    print(add(100, -2))
