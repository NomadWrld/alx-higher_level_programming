#!/usr/bin/python3

class BaseGeometry:
    """
    A base geometry class with area() method and integer_validator() method.
    """

    def area(self):
        """
        Method to calculate the area (not implemented).
        Raises:
            Exception: Always raises an Exception with the message "area() is not implemented".
        """
        raise Exception("area() is not implemented")

    def integer_validator(self, name, value):
        """
        Validate the value to ensure it is an integer and greater than 0.

        Args:
            name (str): The name of the value (used for error message).
            value: The value to be validated.

        Raises:
            TypeError: If the value is not an integer.
            ValueError: If the value is less than or equal to 0.
        """
        if not isinstance(value, int):
            raise TypeError("{} must be an integer".format(name))
        if value <= 0:
            raise ValueError("{} must be greater than 0".format(name))

